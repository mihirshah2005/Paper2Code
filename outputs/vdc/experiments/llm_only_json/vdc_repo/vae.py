"""
vae.py

This module implements the Visual Answer Evaluation (VAE) class for the Versatile Data Cleanser (VDC).
It evaluates the correctness of answers generated by the VQA module by comparing them with an expected answer.
There are two evaluation branches:
  A. Deterministic (label-specific) evaluation using robust string matching.
  B. General evaluation using the ChatGPT API to obtain a strict binary judgment.

The evaluation result (True/False) is later used in a vote-based ensemble to determine the overall matching score.

Dependencies:
  - numpy==1.21.0
  - torch==1.9.0
  - torchvision==0.10.0
  - transformers==4.15.0
  - openai==0.27.0
  - requests==2.26.0
  - PyYAML (for YAML configuration parsing)

Author: Your Name
Date: YYYY-MM-DD
"""

import logging
import re
import time
from typing import Any, Dict

import openai

from utils import load_config, clean_text, init_seed
from vqg import ask_chatgpt  # Reuse the ask_chatgpt API helper from vqg.py

# Configure module-level logger
logger = logging.getLogger(__name__)
logger.setLevel(logging.INFO)


class VAE:
    """
    Visual Answer Evaluation (VAE) module for evaluating the correctness of VQA outputs.
    
    Public Method:
      - evaluate_answer(answer: str, expected: str, question_type: str) -> bool
        Returns True if the provided answer correctly matches the expected answer
        according to the evaluation process, otherwise returns False.
    """

    def __init__(self, config: Dict[str, Any] = None) -> None:
        """
        Initializes the VAE module using configuration settings.
        
        Loads configuration from the provided config dictionary or from "config.yaml" if none is supplied.
        Sets up API parameters for ChatGPT evaluation.
        
        Args:
            config (Optional[Dict[str, Any]]): A dictionary containing configuration parameters.
        """
        if config is None:
            self.config: Dict[str, Any] = load_config("config.yaml")
        else:
            self.config = config

        # Set reproducible seed
        seed_value: int = self.config.get("seed", 42)
        init_seed(seed_value)
        
        # Retrieve ChatGPT API configuration for evaluation
        api_config: Dict[str, Any] = self.config.get("api", {}).get("chatgpt", {})
        self.chatgpt_model: str = api_config.get("model", "gpt-3.5-turbo")
        
        # Set the default evaluation prompt template for general questions.
        # This prompt instructs the evaluation assistant to return only a binary word ("yes" or "no").
        self.evaluation_prompt_template: str = (
            "Assume you are a helpful and precise evaluation assistant. Based solely on the following, "
            "check whether the provided answer matches the expected content. "
            "Please return only a single word, either 'yes' or 'no'.\n"
            "- Answer: '{vqa_answer}'\n"
            "- Expected: '{expected}'"
        )
        
        logger.info(f"VAE initialized with ChatGPT model '{self.chatgpt_model}' and seed {seed_value}.")

    def evaluate_answer(self, answer: str, expected: str, question_type: str) -> bool:
        """
        Evaluates the provided answer against the expected binary value.
        
        Two evaluation branches are implemented:
        
        A. Label-Specific (Deterministic) Evaluation:
           - Normalizes both the answer and expected strings.
           - Uses robust regex matching (with word boundaries) to count occurrences
             of "yes" and "no".
           - Returns True if the answer matches the expected value strictly (i.e.,
             if expected is "yes", at least one distinct "yes" is present and "no" absent,
             and vice versa). In ambiguous cases, returns False.
        
        B. General Evaluation (LLM-Based):
           - Constructs an evaluation prompt using the default template.
           - Calls the ChatGPT API (using ask_chatgpt) to obtain a binary ("yes" or "no") decision.
           - Parses the API response using regex to extract the first occurrence of "yes" or "no".
           - Returns True if the API's binary result matches the expected value.
        
        Args:
            answer (str): The answer text produced by the VQA module.
            expected (str): The expected binary answer ("yes" or "no").
            question_type (str): The type of question ("label-specific" or "general").
        
        Returns:
            bool: True if the evaluated answer aligns with the expected answer; otherwise, False.
        """
        # Type-check all inputs.
        if not isinstance(answer, str) or not isinstance(expected, str) or not isinstance(question_type, str):
            logger.error("Invalid input types for evaluate_answer. Expected strings for answer, expected, and question_type.")
            return False
        
        # Normalize the answer and expected using the provided clean_text utility.
        answer_clean: str = clean_text(answer)
        expected_clean: str = clean_text(expected)
        
        # Validate the expected value should be one of "yes" or "no".
        if expected_clean not in {"yes", "no"}:
            logger.error(f"Expected answer '{expected}' is not valid. It must be either 'yes' or 'no'.")
            return False
        
        # Define regex patterns for exact matches of "yes" and "no" (with word boundaries).
        pattern_yes: str = r"\byes\b"
        pattern_no: str = r"\bno\b"
        
        # Branch for label-specific (deterministic) questions.
        if question_type.lower() == "label-specific":
            count_yes: int = len(re.findall(pattern_yes, answer_clean))
            count_no: int = len(re.findall(pattern_no, answer_clean))
            logger.info(
                f"Label-specific evaluation: normalized answer='{answer_clean}', expected='{expected_clean}', "
                f"count_yes={count_yes}, count_no={count_no}"
            )
            if expected_clean == "yes":
                if count_yes >= 1 and count_no == 0:
                    return True
                else:
                    logger.warning("Label-specific evaluation ambiguous or mismatched (expected 'yes').")
                    return False
            elif expected_clean == "no":
                if count_no >= 1 and count_yes == 0:
                    return True
                else:
                    logger.warning("Label-specific evaluation ambiguous or mismatched (expected 'no').")
                    return False
            else:
                logger.error("Unexpected expected value encountered in label-specific evaluation.")
                return False
        
        # Branch for general (LLM-based) questions.
        elif question_type.lower() == "general":
            # Construct an evaluation prompt using the default prompt template.
            prompt: str = self.evaluation_prompt_template.format(vqa_answer=answer, expected=expected_clean)
            logger.info(f"General evaluation prompt: {prompt}")
            try:
                # Query the ChatGPT API using the helper function from vqg.py.
                api_response: str = ask_chatgpt(prompt, model=self.chatgpt_model)
                api_response_clean: str = clean_text(api_response)
                logger.info(f"ChatGPT API response (normalized): {api_response_clean}")
            except Exception as exc:
                logger.error(f"ChatGPT API call failed during general evaluation: {exc}")
                return False

            # Extract binary token from the API response.
            found_yes = re.search(pattern_yes, api_response_clean)
            found_no = re.search(pattern_no, api_response_clean)
            if found_yes and not found_no:
                api_result: str = "yes"
            elif found_no and not found_yes:
                api_result = "no"
            elif found_yes and found_no:
                logger.warning("Ambiguous API response: both 'yes' and 'no' detected.")
                return False
            else:
                logger.warning("No definitive binary response found in API evaluation response.")
                return False

            if api_result == expected_clean:
                return True
            else:
                logger.info(f"General evaluation mismatch: API result '{api_result}' != expected '{expected_clean}'.")
                return False
        
        else:
            logger.error(f"Unknown question type '{question_type}'. Must be 'label-specific' or 'general'.")
            return False


